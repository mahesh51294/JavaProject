package sample;

public class EvenNum {
	public static void main(String[]args)
	{ 
		int i;
		int count=1;
		System.out.print("List of even numbers from 12 to 42 ");  
		for ( i=12; i<=42; i++)   
		{  
		  
		if (i%2==0)   
		{  
		System.out.print(i +" ");
		System.out.println(count++);
		}  
		}  
		} 
}
	

