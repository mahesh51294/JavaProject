package sample;

public class Player {
	String name;
	int id;
	int age;
	int weight;
	public String getName()
	{
		return name;
	}
	public int getId()
	{
		return id;
	}
	public int getAge()
	{
		return age;
	}
	public int getWeight()
	{
		return weight;
	}
	public void setName(String name)
	{
		this.name=name;
	}
	public void setId(int id)
	{
		this.id=id;
		
	}
	public void setAge(int age)
	{
		this.age=age;
	}
	public void setWeight(int weight)
	{
		this.weight=weight;
	}
}
